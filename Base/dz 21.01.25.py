n = int(input("Введите сколько у вас копеек (от 0 до 99): "))
if 0 <= n <= 99:
    if 11 <= n <= 14:
        print("У вас", n, "копеек")
    else:
        m = n % 10
        if m == 1:
            print("У вас", n, "копейка")
        elif 2 <= m <= 4:
            print("У вас", n, "копейки")
        else:
            print("У вас", n, "копеек")
else:
    print("Ошибка ввода!")